package ProjectManagement.Model;

public class ProjectAllocated {
	private String projOrgName, developersPool, infrastructureTeam, devopsTeam, appSupportTeam, deliveryTeam, systemSupportPool;

	public String getProjOrgName() {
		return projOrgName;
	}

	public void setProjOrgName(String projOrgName) {
		this.projOrgName = projOrgName;
	}

	public String getDevelopersPool() {
		return developersPool;
	}

	public void setDevelopersPool(String developersPool) {
		this.developersPool = developersPool;
	}

	public String getInfrastructureTeam() {
		return infrastructureTeam;
	}

	public void setInfrastructureTeam(String infrastructureTeam) {
		this.infrastructureTeam = infrastructureTeam;
	}

	public String getDevopsTeam() {
		return devopsTeam;
	}

	public void setDevopsTeam(String devopsTeam) {
		this.devopsTeam = devopsTeam;
	}

	public String getAppSupportTeam() {
		return appSupportTeam;
	}

	public void setAppSupportTeam(String appSupportTeam) {
		this.appSupportTeam = appSupportTeam;
	}

	public String getDeliveryTeam() {
		return deliveryTeam;
	}

	public void setDeliveryTeam(String deliveryTeam) {
		this.deliveryTeam = deliveryTeam;
	}

	public String getSystemSupportPool() {
		return systemSupportPool;
	}

	public void setSystemSupportPool(String systemSupportPool) {
		this.systemSupportPool = systemSupportPool;
	}
}
